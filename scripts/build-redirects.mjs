import fs from "node:fs/promises";
import path from "node:path";
import nextEnv from "@next/env";
import { createClient } from "@libsql/client";

const { loadEnvConfig } = nextEnv;
const projectRoot = process.cwd();
loadEnvConfig(projectRoot);

const config = {
  dbUrl: process.env.TURSO_DATABASE_URL,
  dbToken: process.env.TURSO_AUTH_TOKEN,
  outputPath: process.env.REDIRECTS_OUTPUT_PATH || path.join(projectRoot, "lib", "generated-redirects.ts"),
};

async function writeEmptyRedirects() {
  const content = `// Auto-generated by scripts/build-redirects.mjs
export const slugRedirects: Record<string, string> = {};
`;
  await fs.writeFile(config.outputPath, content);
  console.log(`[Redirects] Wrote empty redirects to ${config.outputPath}`);
}

async function main() {
  if (!config.dbUrl) {
    console.warn("[Redirects] TURSO_DATABASE_URL is not set. Writing empty redirects file.");
    await writeEmptyRedirects();
    return;
  }

  const db = createClient({ url: config.dbUrl, authToken: config.dbToken });

  console.log("[Redirects] Loading slug_redirects from database...");
  const result = await db.execute(`
    SELECT old_slug, new_slug FROM slug_redirects ORDER BY old_slug
  `);

  const redirects = {};
  for (const row of result.rows) {
    const oldSlug = row.old_slug;
    const newSlug = row.new_slug;
    if (!oldSlug || !newSlug || oldSlug === newSlug) continue;
    redirects[oldSlug] = newSlug;
  }

  const entries = Object.entries(redirects);
  console.log(`[Redirects] Found ${entries.length} redirect(s).`);

  const lines = entries
    .map(([oldSlug, newSlug]) => `  ${JSON.stringify(oldSlug)}: ${JSON.stringify(newSlug)},`)
    .join("\n");

  const content = `// Auto-generated by scripts/build-redirects.mjs
// Do not edit manually; run "pnpm build:redirects" to regenerate.

export const slugRedirects: Record<string, string> = {
${lines}
};
`;

  await fs.writeFile(config.outputPath, content);
  console.log(`[Redirects] Wrote ${entries.length} redirect(s) to ${config.outputPath}`);
}

main().catch(async (err) => {
  console.error("[Redirects] Failed:", err);
  // Don't block the build; write an empty file so the site still compiles.
  try {
    await writeEmptyRedirects();
  } catch {
    // ignore
  }
});
